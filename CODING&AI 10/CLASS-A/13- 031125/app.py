# Statement if
nilai = 91
#  90-100 : nilai tinggi, 60 - 89 : nilai sedang, 0-59 : nilai rendah

if nilai >= 90 and nilai <=100 :
    print("nilai tinggi")
elif nilai >= 60 and nilai <=89 :
    print("nilai sedang")
elif nilai >= 0 and nilai <=59 :
    print("nilai sedang")
else:
    print("nilai salah !!!!")

